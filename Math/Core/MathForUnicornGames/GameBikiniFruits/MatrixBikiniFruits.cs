        protected static int[,] GetMatixArrayByType(int[][][][] reels, int[][][] reelsSpecial, double[] reelProbs, double[] reelProbsSpec)
        {
            var reelsSet = -1;
            var specReelSet = -1;
            var sum = 0.0;
            var rnd = SoftwareRng.Next();
            var n = reelProbs.Length;
            for (var i = 0; i < n; i++)
            {
                sum += reelProbs[i];
                if (rnd < sum)
                {
                    reelsSet = i;
                    break;
                }
            }
            sum = 0.0;
            rnd = SoftwareRng.Next();
            n = reelProbsSpec.Length;
            for (var i = 0; i < n; i++)
            {
                sum += reelProbsSpec[i];
                if (rnd < sum)
                {
                    specReelSet = i;
                    break;
                }
            }
            return GetMatixArrayByReels(reels[reelsSet], reelsSpecial[specReelSet]);
        }

        protected static int[,] GetMatixArrayByReels(int[][][] reels, int[][] specReels)
        {
            var mat = new int[5, 5];
            var matSpec = new int[5, 5];
            var k = SoftwareRng.Next(reels.Length);
            for (var i = 0; i < 5; i++)
            {
                var l = reels[k][i].Length;
                var p = SoftwareRng.Next(l);
                var lspec = specReels[i].Length;
                var pspec = SoftwareRng.Next(lspec);
                for (var j = 0; j < 5; j++)
                {
                    mat[i, j] = reels[k][i][(p + j) % l];
                    matSpec[i, j] = specReels[i][(pspec + j) % lspec];
                }
            }
            for (var i = 0; i < 5; i++)
            {
                for (var j = 0; j < 5; j++)
                {
                    mat[i, j] = Math.Min(mat[i, j], matSpec[i, j]);
                }
            }
            return mat;
        }

