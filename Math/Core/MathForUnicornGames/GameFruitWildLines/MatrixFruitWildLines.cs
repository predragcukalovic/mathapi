        private static int GetWild(int reelSet)
        {
            var sum = 0.0;
            var rnd = SoftwareRng.Next();
            for (var i = 0; i < 6; i++)
            {
                sum += _WildProbs[reelSet][i];
                if (rnd < sum)
                {
                    return i + 1;
                }
            }
            return 0;
        }

