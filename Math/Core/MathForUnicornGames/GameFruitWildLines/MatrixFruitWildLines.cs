        /// <summary>
        /// Daje 0 ako se ne menjaju simboli, 1-5 ako se menjaju (koliko njih)
        /// </summary>
        /// <returns></returns>
        public int GetLineReplacment(int lineNumber)
        {
            var elem = -1;
            var index = 0;
            var haveWild = false;
            var line = new int[5];
            for (var i = 0; i < 5; i++)
            {
                line[i] = GetElement(i, GameLineFruitWild[lineNumber, i]);
            }
            for (var i = 0; i < 5; i++)
            {
                if (line[i] < 7)
                {
                    elem = line[i];
                    break;
                }
            }
            while (index < 5 && (line[index] > 8 || line[index] == elem))
            {
                if (line[index] > 8)
                {
                    haveWild = true;
                }
                index++;
            }
            if (haveWild)
            {
                return index;
            }
            return 0;
        }

        public void TransformMatrix(int numberOfLines, int wild)
        {
            var replace = new int[numberOfLines];
            for (var i = 0; i < numberOfLines; i++)
            {
                replace[i] = GetLineReplacment(i);
            }
            for (var i = 0; i < numberOfLines; i++)
            {
                if (replace[i] > 2)
                {
                    for (var j = 0; j < replace[i]; j++)
                    {
                        SetElement(j, GameLineFruitWild[i, j], wild);
                    }
                }
            }
            for (var i = 0; i < 5; i++)
            {
                for (var j = 2; j < 5; j++)
                {
                    if (GetElement(i, j) > 8)
                    {
                        SetElement(i, j, wild);
                    }
                }
            }
        }

        public static int[,] GetMatixArray(out int wild)
        {
            var reelsSet = -1;
            var sum = 0.0;
            var rnd = SoftwareRng.Next();
            var n = _ReelProbs.Length;
            for (var i = 0; i < n; i++)
            {
                sum += _ReelProbs[i];
                if (rnd < sum)
                {
                    reelsSet = i;
                    break;
                }
            }
            var mat = new int[5, 7];
            var matSpec = new int[5, 7];
            for (var i = 0; i < 5; i++)
            {
                var l = _Reels[reelsSet][i].Length;
                var p = SoftwareRng.Next(l);
                var lspec = _ReelsSpecial[reelsSet][i].Length;
                var pspec = SoftwareRng.Next(lspec);
                for (var j = 0; j < 7; j++)
                {
                    mat[i, j] = _Reels[reelsSet][i][(p + j) % l];
                    matSpec[i, j] = _ReelsSpecial[reelsSet][i][(pspec + j) % lspec];
                }
            }
            wild = GetWild(reelsSet);
            for (var i = 0; i < 5; i++)
            {
                for (var j = 0; j < 7; j++)
                {
                    if (matSpec[i, j] != 8)
                    {
                        mat[i, j] = wild * 10 + matSpec[i, j];
                    }
                }
            }
            return mat;
        }

