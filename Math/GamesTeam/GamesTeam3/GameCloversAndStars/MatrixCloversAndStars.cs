        public static int[,] GetMatixArray()
        {
            var reelsSet = -1;
            var sum = 0.0;
            var rnd = SoftwareRng.Next();
            var n = _ReelProbs.Length;
            for (var i = 0; i < n; i++)
            {
                sum += _ReelProbs[i];
                if (rnd < sum)
                {
                    reelsSet = i;
                    break;
                }
            }

            int[][] reelSetUsed = _Reels[reelsSet][SoftwareRng.Next(0, _Reels[reelsSet].Length)];

            var mat = new int[5, 6];

            for (var i = 0; i < 5; i++)
            {
                var l = reelSetUsed[i].Length;
                var p = SoftwareRng.Next(l);
                for (var j = 0; j < 6; j++)
                {
                    mat[i, j] = reelSetUsed[i][(p + j) % l];
                }
            }

            return mat;
        }

